(function(){
  var el = document.getElementById('asciiCat');
  if (!el) return;
  var frames = [
    [
      "  /\\_/\\  ",
      " ( o.o ) ",
      "  > ^ <  ",
      " /|   |\\ ",
      "(_|   |_)",
    ],
    [
      "  /\\_/\\  ",
      " ( -.o ) ",
      "  > ^ <  ",
      " /|   |\\ ",
      "(_|   |_)",
    ],
    [
      "  /\\_/\\  ",
      " ( o.o ) ",
      "  > ^ <  ",
      " /|   |\\ ",
      "(_|   |_)",
    ],
    [
      "  /\\_/\\  ",
      " ( o.o ) ",
      "  > ^ <  ",
      " /|   |\\~",
      "(_|   |_)",
    ],
    [
      "  /\\_/\\  ",
      " ( o.o ) ",
      "  > ^ <  ",
      " /|   |\\ ",
      "(_|   |_)~",
    ],
    [
      "  /\\_/\\  ",
      " ( o.o ) ",
      "  > ^ <  ",
      " /|   |\\  ",
      "(_|   |_)~",
    ],
    [
      "  /\\_/\\  ",
      " ( o.- ) ",
      "  > ^ <  ",
      " /|   |\\ ",
      "(_|   |_)",
    ],
  ];
  var i = 0;
  function tick() {
    el.textContent = frames[i].join('\n');
    i = (i + 1) % frames.length;
  }
  tick();
  setInterval(tick, 600);
})();
